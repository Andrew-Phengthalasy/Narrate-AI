"use client";

import { useEffect, useState } from "react";
import { useRouter } from "next/navigation";
import NarrativeViewer from "@/components/NarrativeViewer";
import type { NarrativeResult } from "@/lib/api";

export default function ResultPage() {
  const router = useRouter();
  const [result, setResult] = useState<NarrativeResult | null>(null);

  useEffect(() => {
    const raw = sessionStorage.getItem("narrateResult");
    if (!raw) { router.replace("/"); return; }
    setResult(JSON.parse(raw));
  }, [router]);

  if (!result) {
    return (
      <main className="bg-grid min-h-screen flex items-center justify-center">
        <div className="relative w-12 h-12">
          <div className="absolute inset-0 rounded-full bg-indigo-100 animate-ping opacity-60" />
          <div className="relative w-12 h-12 border-4 border-indigo-500 border-t-transparent rounded-full animate-spin" />
        </div>
      </main>
    );
  }

  return (
    <main className="bg-grid min-h-screen flex flex-col items-center py-12 px-4">
      <div className="w-full max-w-3xl space-y-6">

        {/* ── Top bar ── */}
        <div className="flex items-center justify-between">
          <button
            type="button"
            onClick={() => { sessionStorage.removeItem("narrateResult"); router.push("/"); }}
            className="btn-ghost h-9 px-3 text-xs gap-1.5"
          >
            ← New narrative
          </button>
          <div className="flex items-center gap-2">
            <span className="w-5 h-5 rounded-md bg-indigo-600 flex items-center justify-center">
              <svg width="10" height="10" viewBox="0 0 14 14" fill="none">
                <path d="M2 11 L7 3 L12 11" stroke="white" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/>
                <path d="M4.5 8h5" stroke="white" strokeWidth="2" strokeLinecap="round"/>
              </svg>
            </span>
            <span className="text-xs text-gray-400 font-medium">Powered by IBM Granite</span>
          </div>
        </div>

        <NarrativeViewer result={result} />

        <p className="text-center text-xs text-gray-300 pb-4">Generated by Narrate-AI · IBM AI Skills Challenge 2025</p>

      </div>
    </main>
  );
}
